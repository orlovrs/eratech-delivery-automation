export class Class365Client {
    private appId: number;
    private apiKey: string;
    private domain: string;
    private baseUrl: string;

    constructor(domain: string, appId: number, apiKey: string) {
        this.domain = domain;
        this.appId = appId;
        this.apiKey = apiKey;
        this.baseUrl = `https://${domain}.business.ru/api/rest`;
    }

    static getClient(): Class365Client {
        return new Class365Client(
            Class365Constants.DOMAIN,
            Class365Constants.APP_ID,
            Class365Constants.API_KEY
        );
    }

    public async getToken(): Promise<string> {
        const params = {
            app_id: this.appId
        };
        const paramsStr = QueryStringBuilder.assemble(params, this.apiKey);

        const url = `${this.baseUrl}/${Class365Resources.REPAIR}.json?`
            + paramsStr
            + `&app_psw=${Md5Utils.computeMd5(this.apiKey + paramsStr)}`;
        const response = await fetch(url, {
                method: 'GET',
            }
        );

        if (!response.ok) {
            throw new Error('Could not update token');
        }

        const json = await response.json();
        return json.token;
    }

    public async get(resource: string, params: any): Promise<any> {
        const token = await this.getToken();
        params.app_id = this.appId;
        let paramsStr = QueryStringBuilder.assemble(params, this.apiKey);
        const appPsw = `app_psw=${Md5Utils.computeMd5(token + this.apiKey + paramsStr)}`;
        paramsStr += paramsStr.length == 0 ? `?${appPsw}` : `&${appPsw}`;

        const url = `${this.baseUrl}/${resource}.json?${paramsStr}`;
        const response = await fetch(url, {method: 'GET'});

        if (!response.ok) {
            throw new Error('Could not get deals');
        }

        return await response.json();
    }
}
